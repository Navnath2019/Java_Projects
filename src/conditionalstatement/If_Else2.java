package conditionalstatement;

public class If_Else2 {

	public static void main(String[] args) {
		
		int a = 10; //Local Variable
		
		if (a / 2 ==0)
		{
			// if condition false then statement will not print 
			System.out.println("Result of a is equal to Zero");
		}
		else
		{
			// if condition false then statement will not print 
			System.out.println("Result of a is not equal to Zero");
		}

	}

}
