package loopingstatements;

public class WhileLoop {

	public static void main(String[] args) {
		
	
			int a = 2;
			
			while (a<=20) 
			{
				System.out.println(a);
				a = a+2; //also we can add a++
			}
			System.out.println("****while loop execution has been completed****");
	

	}
}
