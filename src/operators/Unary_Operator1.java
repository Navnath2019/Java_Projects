package operators;

public class Unary_Operator1 {

	public static void main(String[] args) {
		
		//++ operator increase the value by 1
		int a = 1; //Defining Local Variable
		
		System.out.println(a++);//1 2(its in background )  | println says print A and increase next value
		System.out.println(a); //2                         | a + increamented value + same value so printed 2
		System.out.println(a++);// 2 3 increasing          | previews value 2 and increment +1 in background
		System.out.println(++a);// 3+1=4 adding			   | in background value 3 + default value 1= 4 will print
		System.out.println(a);// 4
		
		
		
		
		
		
	}

}
